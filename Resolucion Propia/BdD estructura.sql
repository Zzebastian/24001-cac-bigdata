DROP DATABASE IF EXISTS colegio;
CREATE DATABASE colegio;
USE colegio;
CREATE TABLE `genero` (
    `id_genero` BIGINT PRIMARY KEY,
    `genero` VARCHAR(1024)
);
CREATE TABLE `alumno` (
    `id_alumno` BIGINT PRIMARY KEY,
    `nombre` VARCHAR(1024),
    `apellido` VARCHAR(1024),
    `email` VARCHAR(1024),
    `nota_1p` DOUBLE,
    `nota_2p` DOUBLE,
    `nota_final` DOUBLE NULL,
    `id_genero` BIGINT
);
CREATE TABLE `modelado` (
    `legajo` BIGINT PRIMARY KEY,
    `id_alumno` BIGINT NULL,
    `id_instructor` BIGINT NULL,
    `id_nivel` BIGINT NULL
);
CREATE TABLE `nivel` (
    `id_nivel` BIGINT PRIMARY KEY,
    `nivel` VARCHAR(1024)
);
CREATE TABLE `instructor` (
    `id_instructor` BIGINT PRIMARY KEY,
    `apellido_instructor` VARCHAR(1024) NULL,
    `nombre_instructor` VARCHAR(1024) NULL,
    `email_instructor` VARCHAR(1024) NULL
);
